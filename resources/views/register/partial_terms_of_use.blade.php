第2条（本契約の成立）<br/>
１　申込者が当社に対し、本約款に同意のうえ、当社所定の申込書によって本サービス利用の申込みを行い、当社が申込書に対応する受注書を申込者に交付することにより、本契約が成立するものとします。<br/>
２　前項にかかわらず、本契約にかかる申込みおよび受注は、両当事者が別途合意する場合、電子メール等、他の手段・形式によって行うことができるものとします。<br/>
３　申込者は、申込書記載の事項又はその他当社の指定する事項に変更があった場合、当社の指定する方法にて直ちに届け出るものとします。<br/>

