<h1>ＫＩＤＺＭＥＡＬ Ｐｒｏ利用約款</h1>

<br/>
第1条（定義） <br/>
１　ＫＩＤＺＭＥＡＬ Ｐｒｏ利用約款において使用する以下の用語は、各々以下に定める意味を有するものとします。 <br/>
①	「当社」とは、株式会社ミールケアをいいます。<br/>
②	「本約款」とは、当社が定めるＫＩＤＺＭＥＡＬ Ｐｒｏ利用約款をいいます。<br/>
③	「本サービス」とは、当社が提供する保育園・幼稚園用の管理ソフト「ＫＩＤＺＭＥＡＬ Ｐｒｏ」をいいます。本サービスにおける機能は別紙機能一覧に定める事項をその内容とします。<br/>
④	「申込者」とは、本サービスの利用を希望する、保育園、幼稚園等の運営事業者又は当社が認める者をいいます。<br/>
⑤	「本契約」とは、申込者及び当社間において成立する、本サービスの利用にかかる契約をいいます。<br/>
⑥	「管理ツール」とは、本サービスを利用する上で、申込者が自ら情報を登録・編集できる、当社が提供する専用のウェブページ等をいいます。<br/>
⑦	「園児」とは、申込者が運営する保育園・幼稚園等に通園し、又は通園していた園児等をいいます。 <br/>
⑧	「保護者」とは、園児の保護者をいいます。<br/>
⑨	「保護者向けアプリ」とは、本サービスに関連して当社が提供する、保護者向け保育園・幼稚園連携献立管理アプリ「ＫＩＤＺＭＥＡＬ」をいいます。<br/>
⑩	「知的財産権」とは、著作権、特許権、実用新案権、意匠権、商標権その他の知的財産権（それらの権利を取得し、またはそれらの権利につき登録等を出願する権利を含みます）、肖像権、人格権、パブリシティ権これらに類似・付随・派生する権利を意味します。<br/>
２　前項にかかわらず、本約款中において別途定義を定める場合があります。<br/>

